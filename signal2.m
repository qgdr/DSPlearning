%第二章 信号函数与标准信号
%% 数字电子琴
%正弦波信号发生器
Fs=11025;
A=10;
f=50;
q=60;
T=1;
dt=1/Fs;
N=T/dt;
t=linspace(0,T,N);
y=A*sin(2*pi*f*t+q);
plot(t,y)
%电子琴 131 147 165 175 196 220 247 262 294 330 349 392 440 494 523
%DTMF双音频拨号系统
%摩尔电码
%% 其他波信号
x=square(t,tudy);%duty占空比,默认50%
x=sawtooth(t);%锯齿波
x=sawtooth(t,width);
x=sawtooth(2*pi*3*t);
x=sawtooth(2*pi*3*t,1/2);
x=sawtooth(2*pi*3*t,0);
plot(t,x)
%噪声信号
n=256;
%白噪声
x=randn(1,n);
%均匀噪声
x=rand(1,n);
plot(x)
%其它信号：脉冲，阶跃斜坡，加速度
%脉冲
t=linspace(0,1,101);
y=[zeros(1,50),1,zeros(1,50)];
plot(t,y)
%阶跃
y=[zeros(1,50),ones(1,51)];
plot(t,y);
ylim([-0.2,1.2])
%复合信号
Fs=11025;
T=1;
dt=1/Fs;
N=T/dt;
t=linspace(0,T,N+1);
y1=10*sin(2*pi*392*t);
y2=randn(1,N+1);%噪音
y=y1+0.1*y2;
xlim([0,0.01])
subplot(3,1,1)
plot(t,y1)
subplot(3,1,2)
plot(t,y2)
subplot(3,1,3)
plot(t,y)
soundsc(y,Fs)
%三角波（锯齿波）
y=sawtooth(2*pi*494*t,1/2);
plot(t,y)
xlim([0,0.01])
soundsc(y,Fs)
%方波信号
y=0.3*square(2*pi*392*t);
plot(t,y)
xlim([0,0.01])
sound(y)

